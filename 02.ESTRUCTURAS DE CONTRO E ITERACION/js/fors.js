
console.warn('Estructura for (iteracion)');


/*
! Anatomia del for
* 1- inicializacion -> let i = 0 | se ejecuta una sola vez
* 2- evaluacion -> i <10 | condicion de corte
* 3- incremento/ decremento -> i = i + 1
*/
for (let i = 0; i < 10; i++) {
    console.log(i);
}

console.warn('-------------------------------------------');